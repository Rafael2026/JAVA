public class ejercicio1 {
  public static void main (String[] args) {

    int x = 144;
    int y = 999;

    System.out.println(x + " + " + y + " = " + (x + y) + "\n");

    System.out.println(x + " - " + y + " = " + (x - y) + "\n");

    System.out.println(y + " - " + x + " = " + (y - x) + "\n");

    System.out.println(x + " * " + y + " = " + (x * y) + "\n");

    System.out.println(x + " / " + y + " = " + (x / y) + "\n");

    System.out.println(y + " / " + x + " = " + (y / x) + "\n");

    System.out.println(x + " % " + y + " = " + (x % y) + "\n");
    
    System.out.println(y + " % " + x + " = " + (y % x) + "\n");
  }
}